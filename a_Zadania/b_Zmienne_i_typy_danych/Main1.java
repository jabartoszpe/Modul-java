
public class Main1 {

	public static void main(String[] args) {

		int int1 = 5234456; //zmienna z przedziału liczbowego -2147483648 do 2147483647
		System.out.println(int1);

		char char2 = 'a'; //ta zmienna bazuje na literach i znakach specjalnych
		System.out.println(char2);

		long long3 = 945121545625651l; //zmienna z przedziału -9223372036854775808 do 9223372036854775807
		System.out.println(long3);

		double double4 = 3.54415265562; //zmienna przedział -1,8* 10^108 do 1,8* 10^108
		System.out.println(double4);

		boolean boolean5 = false; //ta zmienna operuje typem "true/false"
		System.out.println(boolean5);
	}

}
